package pojo;

public class LaTimes {

	private String docNo;
	private int docId;
	private StringBuilder date;
	private StringBuilder section;
	private StringBuilder length;
	private StringBuilder headline;
	private StringBuilder byline;
	private StringBuilder text;
	private StringBuilder graphic;
	private StringBuilder type;
	public String getDocNo() {
		return docNo;
	}
	public void setDocNo(String docNo) {
		this.docNo = docNo;
	}
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	public StringBuilder getDate() {
		return date;
	}
	public void setDate(StringBuilder date) {
		this.date = date;
	}
	public StringBuilder getSection() {
		return section;
	}
	public void setSection(StringBuilder section) {
		this.section = section;
	}
	public StringBuilder getLength() {
		return length;
	}
	public void setLength(StringBuilder length) {
		this.length = length;
	}
	public StringBuilder getHeadline() {
		return headline;
	}
	public void setHeadline(StringBuilder headline) {
		this.headline = headline;
	}
	public StringBuilder getByline() {
		return byline;
	}
	public void setByline(StringBuilder byline) {
		this.byline = byline;
	}
	public StringBuilder getText() {
		return text;
	}
	public void setText(StringBuilder text) {
		this.text = text;
	}
	public StringBuilder getGraphic() {
		return graphic;
	}
	public void setGraphic(StringBuilder graphic) {
		this.graphic = graphic;
	}
	public StringBuilder getType() {
		return type;
	}
	public void setType(StringBuilder type) {
		this.type = type;
	}
}